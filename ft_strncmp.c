#include "libft.h"

int     ft_strncmp(char const *s1, char const *s2, size_t n)
{
	int     i;

	i = 0;
	while (s1[i] && s2[i] && s1[i] == s2[i] && n > 0)
	{
		i++;
		n--;
	}
	return ((int)s1[i] - s2[i]);
}
